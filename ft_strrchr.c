#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	x;

	x = ft_strlen(s);
	while (s[x] != c && x > 0)
		x--;
	if (s[x] == c)
		return ((char *)&(s[x]));
	return ((void *)0);
}
